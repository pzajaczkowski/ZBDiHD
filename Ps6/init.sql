create table Time(
id_time NUMBER PRIMARY KEY,
day NUMBER(2),
month NUMBER(2),
year NUMBER(4));

create table Product(
id_product NUMBER PRIMARY KEY,
name VARCHAR2(50)
);

create table Location(
id_location NUMBER PRIMARY KEY,
zip VARCHAR2(5),
city VARCHAR2(30),
country VARCHAR2(30),
continent VARCHAR2(50));

create table SaleType(
id_type NUMBER PRIMARY KEY,
name VARCHAR2(30));

create table Category(
id_category NUMBER PRIMARY KEY,
name VARCHAR2(30));

create table Age(
id_age NUMBER PRIMARY KEY,
age_from NUMBER(3),
age_to NUMBER(3),
name VARCHAR2(20)
);

create table Sale(
id_sale NUMBER PRIMARY KEY,
id_product NUMBER REFERENCES Product(id_product),
id_location NUMBER REFERENCES Location(id_location),
id_age NUMBER REFERENCES Age(id_age),
id_time NUMBER REFERENCES Time(id_time),
id_type NUMBER REFERENCES SaleType(id_type),
id_category NUMBER REFERENCES Category(id_category),
how_many NUMBER,
amount NUMBER,
profit NUMBER
);

create table TimeMonth(
id_time NUMBER PRIMARY KEY,
month NUMBER(2),
year NUMBER(4)
);

create table AdType(
id_type NUMBER PRIMARY KEY,
description VARCHAR2(50)
);

create table Advertizement(
id_advertizement NUMBER PRIMARY KEY,
id_type NUMBER REFERENCES AdType(id_type),
id_time NUMBER REFERENCES TimeMonth(id_time),
id_product NUMBER REFERENCES Product(id_product),
avg_duration NUMBER,
budget NUMBER
);


